-- 오랜 기간 보호한 동물(1)
SELECT NAME, DATETIME 
FROM ANIMAL_INS
WHERE ANIMAL_ID NOT IN (SELECT A.ANIMAL_ID FROM ANIMAL_OUTS A)
ORDER BY DATETIME DESC LIMIT 3


-- LEFT JOIN 사용
SELECT INS.NAME , INS.DATETIME
FROM ANIMALINS INS
  LEFT JOIN ANIMALOUTS OUTS ON INS.ANIMALID = OUTS.ANIMALID 
WHERE OUTS.ANIMAL_ID IS NULL
ORDER BY DATETIME LIMIT 3