-- 노선별 평균 역 사이 거리 조회하기
SELECT ROUTE, CONCAT(ROUND(SUM(D_BETWEEN_DIST), 1), "km") TOTAL_DISTANCE, CONCAT(ROUND(SUM(D_BETWEEN_DIST)/COUNT(NO), 2), "km") AVERAGE_DISTANCE
FROM SUBWAY_DISTANCE
GROUP BY ROUTE
ORDER BY ROUND(SUM(D_BETWEEN_DIST), 1) DESC
-- ORDER BY TOTAL_DISTANCE DESC
